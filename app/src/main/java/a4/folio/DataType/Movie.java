public class Movie {
    private String name , date , length , size , link , detail ;

    public String getName() {
        return name;
    }

    public String getDate() {
        return date;
    }

    public String getLength() {
        return length;
    }

    public String getSize() {
        return size;
    }

    public String getLink() {
        return link;
    }

    public String getDetail() {
        return detail;
    }
}
